package com.baseframework.error;

public class FieldError {

	private String fieldId;
	
	private String fieldName;
	
	private String fieldErrorCode;

	private String filedErrorMessage;

	public String getFieldId() {
		return fieldId;
	}

	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getFieldErrorCode() {
		return fieldErrorCode;
	}

	public void setFieldErrorCode(String fieldErrorCode) {
		this.fieldErrorCode = fieldErrorCode;
	}

	public String getFiledErrorMessage() {
		return filedErrorMessage;
	}

	public void setFiledErrorMessage(String filedErrorMessage) {
		this.filedErrorMessage = filedErrorMessage;
	}


}
